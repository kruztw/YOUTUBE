#include <stdio.h>

int main() {
    // integer
    // var: variable
    int var_int = 1;
    printf("var_int = %d\n", var_int); // decimal (十進制)

    // character
    char var_char = 'a';
    printf("var_char = %c\n", var_char);

    // 浮點數 (floating point)
    float var_float = 1.5;
    printf("var_float = %f\n", var_float);

    // 浮點數
    double var_double = 1.6;
    printf("var_double = %lf\n", var_float); // long float

    return 0;
}

